//Pratica do Laço While. 

let transformNumber = 5000;
console.log("---- Exercicio de Loops: Usando While ----");

while (transformNumber >= 2) {
    console.log(`Número atual: ${transformNumber}`);
    transformNumber -= 1;
}

console.log(`O resultado final é o esperado: ${transformNumber}`);

console.log("---- Fim do Exercicio ---- ");
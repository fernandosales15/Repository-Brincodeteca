console.log(`---- Exerçicio de Loops: Praticando o Do While ----- `); 

let contador = 9999;

do { 
    console.log(`O número atual do contador é: ${contador}`);
    contador --;
} while (contador > 0);

console.log(`---- Fim do Exercício de Do While, o Contador chegou ao número esperado de Zero ---- `);